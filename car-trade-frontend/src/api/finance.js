export const getDeposit = () => uni.http.get('/finance/deposit')
export const rechargeDeposit = (data) => uni.http.post('/finance/deposit/recharge', data)
export const withdrawDeposit = (data) => uni.http.post('/finance/deposit/withdraw', data)
export const getDepositRecords = (params) => uni.http.get('/finance/deposit/records', { params })
export const getCredit = () => uni.http.get('/finance/credit')
export const applyCredit = (data) => uni.http.post('/finance/credit/apply', data)
